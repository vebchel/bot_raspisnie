def format_schedule(para: str, predmet: str, auditoriya: str, prepodavatel: str) -> str:
    """–§–æ—Ä–º–∞—Ç–∏—Ä—É–µ—Ç —Å—Ç—Ä–æ–∫—É —Ä–∞—Å–ø–∏—Å–∞–Ω–∏—è —Å –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ–º —Å–º–∞–π–ª–∏–∫–æ–≤."""
    # –°–ª–æ–≤–∞—Ä—å —Å–º–∞–π–ª–∏–∫–æ–≤ –¥–ª—è —Ä–∞–∑–Ω—ã—Ö –ø—Ä–µ–¥–º–µ—Ç–æ–≤ (–º–æ–∂–µ—à—å —Ä–∞—Å—à–∏—Ä–∏—Ç—å!)
    emoji_for_subject = {
        "–ú–∞—Ç–µ–º–∞—Ç–∏–∫–∞": "üìê",
        "–§–∏–∑–∏–∫–∞": "üß™",
        "–ò–Ω—Ñ–æ—Ä–º–∞—Ç–∏–∫–∞": "üíª",
        "–ò—Å—Ç–æ—Ä–∏—è": "üìú",
        "–ê–Ω–≥–ª–∏–π—Å–∫–∏–π": "üá¨üáß",
        "–§–∏–∑–∫—É–ª—å—Ç—É—Ä–∞": "üèÉ‚Äç‚ôÇÔ∏è",
        "–ü—Ä–æ–≥—Ä–∞–º–º–∏—Ä–æ–≤–∞–Ω–∏–µ": "üë®‚Äçüíª",
        "–ü—Ä–∞–∫—Ç–∏–∫–∞": "üõ†Ô∏è",  # –î–æ–±–∞–≤–∏–º —Å–º–∞–π–ª–∏–∫ –¥–ª—è –ø—Ä–∞–∫—Ç–∏–∫–∏
        "–ú–î–ö.02.01 –ú–æ–Ω—Ç–∞–∂ –∏ –æ–±—Å–ª—É–∂–∏–≤–∞–Ω–∏–µ –∏–Ω—Ñ–æ–∫–æ–º–º—É–Ω–∏–∫–∞—Ü–∏–æ–Ω–Ω—ã—Ö —Å–∏—Å—Ç–µ–º —Å –∫–æ–º–º—É—Ç–∞—Ü–∏–µ–π –ø–∞–∫–µ—Ç–æ–≤ –∏ –∫–∞–Ω–∞–ª–æ–≤": "üì°" #–°–º–∞–π–ª–∏–∫ –¥–ª—è —ç—Ç–æ–≥–æ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–≥–æ –ø—Ä–µ–¥–º–µ—Ç–∞
    }
    # –í—ã–±–∏—Ä–∞–µ–º —Å–º–∞–π–ª–∏–∫ –¥–ª—è –ø—Ä–µ–¥–º–µ—Ç–∞ (–µ—Å–ª–∏ –µ—Å—Ç—å, –∏–Ω–∞—á–µ –∏—Å–ø–æ–ª—å–∑—É–µ–º "–∫–Ω–∏–≥—É")
